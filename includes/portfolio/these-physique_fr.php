    <h2>Modélisation Prédictive de Systèmes Quantiques Complexes</h2>
    
    <div class="portfolio-content">
        <div class="portfolio-description">
            <p><strong>Le Défi :</strong> Atteindre la précision chimique (~0.043 eV), pour prédire les propriétés électroniques de matériaux organiques comme les acènes, un challenge majeur pour les méthodes de simulation numérique, qui doivent être comparées et validées entre elles. De telles simulations de haute précision sont indispensables dans la recherche de matériaux prometteurs pour des applications d'avenir comme la spintronique ou le stockage d'énergie.</p>
            <p><strong>Ma Solution :</strong> Mon approche a été de modéliser les états électroniques fondamentaux d'une série de molécules de longueur croissante (les acènes). Pour chaque molécule, ma méthode a été de lancer des simulations intensives via la méthode Monte Carlo Quantique, d'extraire et comparer les niveaux d'énergie, puis d'analyser les géométries et densités électroniques pour valider les modèles.</p>
            <p><strong>Résultat & Impact :</strong> Ce travail de recherche a abouti à 2 publications dans des revues internationales, validant une méthodologie permettant une analyse fine et une estimation précise de la géométrie des molécules étudiées. J'y ai personnellement consolidé une expertise consistant à lier les simulations numériques et le réel qu'elles doivent représenter, tout en quantifiant les risques d'erreur.</p>
