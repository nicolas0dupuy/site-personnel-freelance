    <h2>Predictive Modeling of Complex Quantum Systems</h2>
    
    <div class="portfolio-content">
        <div class="portfolio-description">
            <p><strong>The Challenge:</strong> To achieve chemical accuracy (~0.043 eV) in order to predict the electronic properties of organic materials such as acenes, a major challenge for numerical simulation methods, which must be benchmarked and validated against each other. Such high-precision simulations are essential in the search for promising materials for future applications like spintronics or energy storage.</p>
            <p><strong>My Solution:</strong> My approach was to model the ground electronic states of a series of molecules of increasing length (the acenes). For each molecule, my method was to run intensive simulations using the Quantum Monte Carlo method, extract and compare the energy levels, and then analyze the molecular geometries and electronic densities to validate the models.</p>
            <p><strong>Outcome & Impact:</strong> This research work resulted in 2 publications in international journals, validating a methodology that allows for a fine-grained analysis and precise estimation of the studied molecules' geometry. Through it, I personally consolidated an expertise in linking numerical simulations with the reality they are meant to represent, while also quantifying the risks of error.</p>
