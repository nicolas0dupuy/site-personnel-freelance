    <h2>Doctorat en Physique et Chimie des Matériaux</h2>
    <p class="parcours-meta">
        <strong>Sujet :</strong> Corrélations électroniques des acènes vers la limite de longue taille : étude par Monte Carlo quantique <br>
        <strong>Laboratoire :</strong> Institut de Minéralogie, de Physique des Matériaux et de Cosmochimie (IMPMC), Paris | 2012 - 2016
    </p>
    
    <p>
        Sous la supervision du Pr. Michele Casula et du Pr. Francesco Mauri, mon travail de thèse s'est concentré sur la prédiction des propriétés électroniques de molécules - les acènes - précurseurs des nanorubans de graphène.
    </p>
    <p>
        L'objectif était de mieux comprendre par simulations numériques les comportements quantiques pouvant mener à des applications futures en spintronique ou pour le stockage d'énergie.
    </p>
    <p>
        Ce travail de recherche et l'exigence de mes mentors m'ont permis de développer un ensemble de compétences fondamentales :
    </p>
    <ul>
        <li><strong>Maîtrise du cycle de modélisation :</strong> Conceptualisation d'un problème physique complexe, implémentation de modèles, analyse critique des résultats et confrontation avec l'état de l'art.</li>
        <li><strong>Analyse et interprétation de grands volumes de données :</strong> Traitement et extraction de la valeur issue des données massives générées par les simulations numériques.</li>
        <li><strong>Communication technique :</strong> Synthèse et diffusion des résultats, rédaction d'articles de journaux spécialisées.</li>
    </ul>

