    <h2>Ouverture sur le Monde : Langues et Cultures</h2>
    <p class="parcours-meta">
        <strong>Compétences :</strong> Apprentissage continu et passionné de plusieurs langues
    </p>

    <p>
        Ma passion pour les langues va au-delà de la compétence technique. C'est la voie vers de nouvelles cultures, et de nouvelles rencontres. Chaque langue ouvre l'espoir de nouvelles rencontres.
    </p>

    <details>
        <summary>Détail de mon parcours linguistique</summary>
        <div class="details-content">
            <h4>Niveaux et Histoires</h4>
            <ul>
                <li><strong>Français :</strong> Langue maternelle.</li>
                <li><strong>Anglais (Niveau B2-C1) :</strong> Lecture scientifique courante, pratique régulière.</li>
                <li><strong>Russe (Niveau B1-B2) :</strong> Deux années d'étude intensive à l'INALCO en parallèle de ma maîtrise de maths. Je continue de l'étudier, notamment à travers la poésie.</li>
                <li><strong>Allemand (Niveau B1-B2) :</strong> Un apprentissage de lycée que je ravive périodiquement par amour pour la langue, même si la pratique orale reste ardue.</li>
                <li><strong>Estonien (Niveau A2-B1) :</strong> Mon grand amour actuel ! Je séjourne chaque été à Tallinn et m'améliore en retrouvant mes amis. Je lis Sipsik depuis 4 ans et ne suis pas pressé de le finir! J'aime la poésie de Jaan Kaplinski.</li>
                <li><strong>Tchèque & Hongrois (Niveau A2) :</strong> Appris intensivement en autodidacte pour des voyages réguliers il y a quelques années. J'ai aimé le défi intellectuel d'apprendre en parallèle ces deux langues si riches différentes.</li>
                <li><strong>Suédois (Débutant) :</strong> Une passion pour la langue, nourrie par les excellents thrillers scandinaves comme Brön (dans lequel Sofia Helin était formidable).</li>
                <li><strong>Italien (Niveau A2) :</strong> Comment ne pas aimer cette langue ! Surtout avec une équipe de thèse majoritairement italienne.</li>
                <li><strong>Chinois (Débutant) :</strong> Une initiation lente depuis plusieurs années. J'ai enfin trouvé un plaisir immense à apprendre les caractères grâce à une correspondante et amie en ligne, étudiant, elle, le français.</li>
            </ul>

            <hr style="border-color: rgba(255,255,255,0.1); margin: 20px 0;">

            <h4>Anecdotes sur l'apprentissage</h4>
            <div class="anecdote-item" style="margin-top:0; margin-bottom: 25px;">
                <p>“J'ai commencé le russe à 25 ans, initialement pour me sortir la tête des mathématiques. J'y ai découvert le plaisir de rencontrer de nouvelles personnes et de comprendre une autre culture. Bien plus que la recherche de performance pour un CV, apprendre les langues me permet d'être là pour l'autre.”</p>
            </div>
            <div class="anecdote-item" style="margin-top:0; margin-bottom: 25px;">
                <p>“En tant que neuro-atypique, j'ai longtemps refoulé mon envie de parler par crainte du jugement. Apprendre une langue étrangère a été libérateur. Les efforts sont souvent accueillis avec bienveillance par les autres. Ça m'a redonné le courage de m'exprimer, même si le jugement sur mes intonations parfois bizarres reste pour moi un défi en français ou en anglais.”</p>
