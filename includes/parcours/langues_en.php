    <h2>Opening up to the World: Languages and Cultures</h2>
    <p class="parcours-meta">
        <strong>Skills:</strong> Continuous and passionate learning of several languages
    </p>

    <p>
        My passion for languages goes beyond technical skill. It is the path to new cultures and new encounters. Each language opens up the hope for new connections.
    </p>

    <details>
        <summary>Details of my linguistic journey</summary>
        <div class="details-content">
            <h4>Levels and Stories</h4>
            <ul>
                <li><strong>French:</strong> Native speaker.</li>
                <li><strong>English (Level B2-C1):</strong> Fluent in reading scientific literature, practiced regularly.</li>
                <li><strong>Russian (Level B1-B2):</strong> Two years of intensive study at INALCO alongside my Master's in Mathematics. I continue to study it, notably through poetry.</li>
                <li><strong>German (Level B1-B2):</strong> Learned in high school, I periodically revive it out of love for the language, even if speaking remains arduous.</li>
                <li><strong>Estonian (Level A2-B1):</strong> My current great love! I stay in Tallinn every summer and improve by meeting up with my friends. I've been reading the children's book "Sipsik" for 4 years and I'm in no hurry to finish it! I love the poetry of Jaan Kaplinski.</li>
                <li><strong>Czech & Hungarian (Level A2):</strong> Taught myself intensively for regular trips a few years ago. I loved the intellectual challenge of learning these two rich and different languages in parallel.</li>
                <li><strong>Swedish (Beginner):</strong> A passion for the language, fueled by excellent Scandinavian thrillers like "Brön/Broen" (in which Sofia Helin was formidable).</li>
                <li><strong>Italian (Level A2):</strong> How can one not love this language! Especially with a PhD team that was predominantly Italian.</li>
                <li><strong>Chinese (Beginner):</strong> A slow start over several years. I finally found immense pleasure in learning the characters thanks to an online pen pal and friend, who in turn was learning French.</li>
            </ul>

            <hr style="border-color: rgba(255,255,255,0.1); margin: 20px 0;">

            <h4>Learning Anecdotes</h4>
            <div class="anecdote-item" style="margin-top:0; margin-bottom: 25px;">
                <p>“I started Russian at 25, initially to 'get my mind off' mathematics. In it, I discovered the pleasure of meeting new people and understanding another culture. Much more than seeking a skill for a resume, learning languages allows me to be there for others.”</p>
            </div>
            <div class="anecdote-item" style="margin-top:0; margin-bottom: 25px;">
                <p>“As a neuro-atypical person, I suppressed my desire to speak for a long time, for fear of being judged. Learning a foreign language was liberating. Far from home, efforts are often met with kindness by others. It gave me the courage to express myself again, even if being judged for my sometimes 'weird' intonations remains a challenge for me in French or English.”</p>
            </div>
