            </p>
            <p>
                <strong>Maîtrise (M1) :</strong> Analyse fonctionnelle et théorie spectrale, algèbre et théorie de Galois, probabilités, géométrie différentielle.
            </p>
            <p>
                <strong>Master 2 (validé partiellement) :</strong> Systèmes dynamiques, Algèbre et groupes de Lie.
