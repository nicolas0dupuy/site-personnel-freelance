    <h2>Cursus de Mathématiques (Licence & Master)</h2>
    <p class="parcours-meta">
        <strong>Période :</strong> 2002 - 2005 (et matières isolées ultérieurement)
    </p>

    <p>
        J'ai étudié les mathématiques fondamentales, validant une Licence avec mention Bien (à 0.25 points de la mention Très Bien) et une Maîtrise avec mention Assez Bien, ainsi que des matières de Master 2. Ce parcours a forgé mes capacités de raisonnement, d'abstraction et l'art de la démonstration.
    </p>

    <details>
